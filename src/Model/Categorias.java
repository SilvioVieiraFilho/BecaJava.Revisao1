package Model;

public class Categorias {

	public int Id_categoria;
	public String nome;

	public int getId_categoria() {
		return Id_categoria;
	}

	public void setId_categoria(int id_categoria) {
		Id_categoria = id_categoria;
	}

	public String getNome() {
		return nome;
	}

	public void setNome(String nome) {
		this.nome = nome;
	}

}
